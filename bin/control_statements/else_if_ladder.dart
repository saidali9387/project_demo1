void main(){
  String size='L';

  if(size=='Xs'){
    print("Size is Xs");
  }else if(size=='XL'){
    print("Size is XL");
  }else if(size=='M'){
    print("Size is M");
  }else if(size=='L'){
    print("Size is L");
  }else if(size=='XXL'){
    print("Size is XXL");
  }else{
    print("Size not available");
  }
}